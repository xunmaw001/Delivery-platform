					return
				}
																																								if((!this.ruleForm.shangjiaxingming)){
					this.$utils.msg(`商家姓名不能为空`);
					return
				}
																																																																																																																																																								if(this.ruleForm.shangjiayouxiang&&(!this.$validate.isEmail(this.ruleForm.shangjiayouxiang))){
					this.$utils.msg(`商家邮箱应输入邮件格式`);
					return
				}
																																																																																												if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																												if(this.ruleForm.money&&(!this.$validate.isNumber(this.ruleForm.money))){
					this.$utils.msg(`余额应输入数字`);
					return
				}
